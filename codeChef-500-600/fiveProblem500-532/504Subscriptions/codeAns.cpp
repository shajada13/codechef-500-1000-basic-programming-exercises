/*
Key C++ constructs used:

1. while          -> লুপ স্টেটমেন্ট
2. অপারেটর (+, /, *) -> অরিথমেটিক এবং তুলনা (যেমন গাণিতিক হিসাব)
*/


#include <bits/stdc++.h>
using namespace std;

int main() {
    int T;
    cin >> T;

    while (T--) {
        int N, X;
        cin >> N >> X;
        int subs = (N + 5) / 6; 
        cout << subs * X << endl;
    }

    return 0 ;
}

/*
কোডের কাজ:

1. প্রথমে T ইনপুট নেওয়া হয়, যা টেস্ট কেসের সংখ্যা।
2. প্রতিটি টেস্ট কেসে N (মোট পেজ/আইটেম সংখ্যা) এবং X (একটি সাবস্ক্রিপশনের দাম) ইনপুট নেওয়া হয়।
3. (N + 5) / 6 হিসাব করে মোট কতটি সাবস্ক্রিপশন লাগবে তা বের করা হয়। 
   - এখানে 6 মানে প্রতি সাবস্ক্রিপশন 6 ইউনিট কভার করে।
   - N + 5 ব্যবহার করা হয়েছে যাতে ভাগশেষ থাকলেও একটি অতিরিক্ত সাবস্ক্রিপশন নেওয়া হয়।
4. তারপর subs * X করে মোট খরচ বের করা হয় এবং আউটপুট দেওয়া হয়।
*/

